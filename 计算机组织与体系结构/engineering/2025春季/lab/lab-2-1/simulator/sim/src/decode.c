#include <common.h>
#include <macro.h>
#include <pattern.h>
#include <cpu.h>
#include <memory.h>

extern CPU_state cpu;
extern int debug_mode;

#define R(i) (cpu.reg[i])
#define Mr mem_read
#define Mw mem_write
#define HALT(thispc, code) halt_trap(thispc, code)

enum {
  TYPE_I, TYPE_U, TYPE_S,
  TYPE_R, TYPE_J, TYPE_B,
  TYPE_N,
};

#define src1R() do { *src1 = R(rs1); } while (0)
#define src2R() do { *src2 = R(rs2); } while (0)
#define immI() do { *imm = SEXT(BITS(i, 31, 20), 12); } while(0)
#define immU() do { *imm = SEXT(BITS(i, 31, 12), 20) << 12; } while(0)
#define immJ() do { *imm = SEXT(BITS(i, 31, 31) << 20 | \
                                BITS(i, 19, 12) << 12 | \
                                BITS(i, 20, 20) << 11 | \
                                BITS(i, 30, 21) << 1, 21);} while(0)
#define immS() do { *imm = (SEXT(BITS(i, 31, 25), 7) << 5) | BITS(i, 11, 7); } while(0)
#define immB() do { *imm = SEXT(BITS(i, 31, 31) << 12 | \
                                BITS(i, 7,  7) << 11 | \
                                BITS(i, 30, 25) << 5 | \
                                BITS(i, 11,  8) << 1, 13); } while(0)
#define immR() do { *imm = BITS(i, 31, 25); } while(0)

#define TRUNC(s) (SEXT(BITS((s), 31, 0), 32))

static void decode_operand(Decode *s, int *rd, uint64_t *src1, uint64_t *src2, uint64_t *imm, int type){
    uint32_t i = s->inst;
    int rs1 = BITS(i, 19, 15);
    int rs2 = BITS(i, 24, 20);
    *rd     = BITS(i, 11,  7);
    switch(type) {
        case TYPE_I: src1R();          immI(); break;
        case TYPE_U:                   immU(); break;
        case TYPE_J:                   immJ(); break;
        case TYPE_S: src1R(); src2R(); immS(); break;
        case TYPE_B: src1R(); src2R(); immB(); break;
        case TYPE_R: src1R(); src2R(); immR(); break;
    }
}
void decode_exec(Decode *s){
    int rd = 0;
    uint64_t src1 = 0, src2 = 0, imm = 0;
    s->dnpc = s->snpc;

#define INSTPAT_INST(s) ((s)->inst)
#define INSTPAT_MATCH(s, name, type, ... /* execute body */ ) { \
  decode_operand(s, &rd, &src1, &src2, &imm, concat(TYPE_, type)); \
  __VA_ARGS__ ; \
}
    INSTPAT_START();
    if(debug_mode)printf(ANSI_FMT("Executing PC: 0x%08lx ins=%lx\n", ANSI_FG_RED), s->pc, s->inst);

    //RV64I

    //OPCODE_LUI
    INSTPAT("??????? ????? ????? ??? ????? 01101 11", lui    , U, R(rd) = imm);
    //OPCODE_AUIPC
    INSTPAT("??????? ????? ????? ??? ????? 00101 11", auipc  , U, R(rd) = s->pc + imm);
    //OPCODE_JAL
    INSTPAT("??????? ????? ????? ??? ????? 11011 11", jal    , J, R(rd) = s->pc + 4, s->dnpc = s->pc + imm);
    //OPCODE_JALR
    INSTPAT("??????? ????? ????? 000 ????? 11001 11", jalr   , I, R(rd) = s->pc + 4, s->dnpc = (src1 + imm) & ~1ull);
    //OPCODE_BRANCH
    INSTPAT("??????? ????? ????? 000 ????? 11000 11", beq    , B, s->dnpc = src1 == src2 ? s->pc + imm : s->dnpc);
    INSTPAT("??????? ????? ????? 001 ????? 11000 11", bne    , B, s->dnpc = src1 != src2 ? s->pc + imm : s->dnpc);
    INSTPAT("??????? ????? ????? 100 ????? 11000 11", blt    , B, s->dnpc = (int64_t)src1 <  (int64_t)src2 ? s->pc + imm : s->dnpc);
    INSTPAT("??????? ????? ????? 101 ????? 11000 11", bge    , B, s->dnpc = (int64_t)src1 >= (int64_t)src2 ? s->pc + imm : s->dnpc);
    INSTPAT("??????? ????? ????? 110 ????? 11000 11", bltu   , B, s->dnpc = src1 <  src2 ? s->pc + imm : s->dnpc);
    INSTPAT("??????? ????? ????? 111 ????? 11000 11", bgeu   , B, s->dnpc = src1 >= src2 ? s->pc + imm : s->dnpc);
    //OPCODE_LOAD
    INSTPAT("??????? ????? ????? 000 ????? 00000 11", lb     , I, R(rd) = SEXT(Mr(src1 + imm, 1), 8));
    INSTPAT("??????? ????? ????? 001 ????? 00000 11", lh     , I, R(rd) = SEXT(Mr(src1 + imm, 2), 16));
    INSTPAT("??????? ????? ????? 010 ????? 00000 11", lw     , I, R(rd) = SEXT(Mr(src1 + imm, 4), 32));
    INSTPAT("??????? ????? ????? 100 ????? 00000 11", lbu    , I, R(rd) = Mr(src1 + imm, 1));
    INSTPAT("??????? ????? ????? 101 ????? 00000 11", lhu    , I, R(rd) = Mr(src1 + imm, 2));
    INSTPAT("??????? ????? ????? 110 ????? 00000 11", lwu    , I, R(rd) = Mr(src1 + imm, 4));
    INSTPAT("??????? ????? ????? 011 ????? 00000 11", ld     , I, R(rd) = Mr(src1 + imm, 8));
    //OPCODE_STORE
    INSTPAT("??????? ????? ????? 000 ????? 01000 11", sb     , S, Mw(src1 + imm, 1, src2));
    INSTPAT("??????? ????? ????? 001 ????? 01000 11", sh     , S, Mw(src1 + imm, 2, src2));
    INSTPAT("??????? ????? ????? 010 ????? 01000 11", sw     , S, Mw(src1 + imm, 4, src2));
    INSTPAT("??????? ????? ????? 011 ????? 01000 11", sd     , S, Mw(src1 + imm, 8, src2));
    //OPCODE_OPIMM
    INSTPAT("??????? ????? ????? 000 ????? 00100 11", addi   , I, R(rd) = src1 + imm);
    INSTPAT("??????? ????? ????? 010 ????? 00100 11", slti   , I, R(rd) = (int64_t)src1 <  (int64_t)imm ? 1 : 0);
    INSTPAT("??????? ????? ????? 011 ????? 00100 11", sltiu  , I, R(rd) = src1 <  imm ? 1 : 0);
    INSTPAT("??????? ????? ????? 100 ????? 00100 11", xori   , I, R(rd) = src1 ^ imm);
    INSTPAT("??????? ????? ????? 110 ????? 00100 11", ori    , I, R(rd) = src1 | imm);
    INSTPAT("??????? ????? ????? 111 ????? 00100 11", andi   , I, R(rd) = src1 & imm);
    INSTPAT("??????? ????? ????? 001 ????? 00100 11", slli   , I, R(rd) = src1 << imm);
    INSTPAT("000000? ????? ????? 101 ????? 00100 11", srli   , I, R(rd) = src1 >> imm);
    INSTPAT("010000? ????? ????? 101 ????? 00100 11", srai   , I, R(rd) = (int64_t)src1 >> BITS(imm, 5, 0));
    //OPCODE_OPIMM32
    INSTPAT("??????? ????? ????? 000 ????? 00110 11", addiw  , I, R(rd) = TRUNC(BITS(src1 +  imm, 31, 0)));
    INSTPAT("??????? ????? ????? 001 ????? 00110 11", slliw  , I, R(rd) = TRUNC(BITS(src1 << imm, 31, 0)));
    INSTPAT("0000000 ????? ????? 101 ????? 00110 11", srliw  , I, R(rd) = TRUNC(BITS(src1, 31, 0) >> imm));
    INSTPAT("0100000 ????? ????? 101 ????? 00110 11", sraiw  , I, R(rd) = TRUNC(((int32_t)BITS(src1, 31, 0) >> BITS(imm, 4, 0))));
    //OPCODE_OP
    INSTPAT("0000000 ????? ????? 000 ????? 01100 11", add    , R, R(rd) = src1 + src2);
    INSTPAT("0100000 ????? ????? 000 ????? 01100 11", sub    , R, R(rd) = src1 - src2);
    INSTPAT("0000000 ????? ????? 001 ????? 01100 11", sll    , R, R(rd) = src1 << BITS(src2, 5, 0));
    INSTPAT("0000000 ????? ????? 010 ????? 01100 11", slt    , R, R(rd) = (int64_t)src1 < (int64_t)src2 ? 1 : 0);
    INSTPAT("0000000 ????? ????? 011 ????? 01100 11", sltu   , R, R(rd) = src1 <  src2 ? 1 : 0);
    INSTPAT("0000000 ????? ????? 100 ????? 01100 11", xor    , R, R(rd) = src1 ^ src2);
    INSTPAT("0000000 ????? ????? 101 ????? 01100 11", srl    , R, R(rd) = src1 >> BITS(src2, 5, 0));
    INSTPAT("0100000 ????? ????? 101 ????? 01100 11", sra    , R, R(rd) = (int64_t)src1 >> BITS(src2, 5, 0));
    INSTPAT("0000000 ????? ????? 110 ????? 01100 11", or     , R, R(rd) = src1 | src2);
    INSTPAT("0000000 ????? ????? 111 ????? 01100 11", and    , R, R(rd) = src1 & src2);
    //OPCODE_OP32
    
    INSTPAT("0000000 ????? ????? 000 ????? 01110 11", addw   , R, R(rd) = TRUNC(src1 + src2));
    INSTPAT("0100000 ????? ????? 000 ????? 01110 11", subw   , R, R(rd) = TRUNC(src1 - src2));
    INSTPAT("??????? ????? ????? 001 ????? 01110 11", sllw   , R, R(rd) = TRUNC(src1 << BITS(src2, 4, 0)));
    INSTPAT("0000000 ????? ????? 101 ????? 01110 11", srlw   , R, R(rd) = TRUNC(BITS(src1, 31, 0) >> BITS(src2, 4, 0)));
    INSTPAT("0100000 ????? ????? 101 ????? 01110 11", sraw   , R, R(rd) = TRUNC(((int32_t)BITS(src1, 31, 0) >> BITS(src2, 4, 0))));
    //OPCODE_FENCE  

    //OPCODE SYSTEM
    INSTPAT("0000000 00001 00000 000 00000 11100 11", ebreak , N, HALT(s->pc, R(10))); // R(10) is $a0

    //RV64M
    INSTPAT("0000001 ????? ????? 000 ????? 01100 11", mul    , R, R(rd) = (int64_t)((int64_t)src1 * (int64_t)src2));
    INSTPAT("0000001 ????? ????? 001 ????? 01100 11", mulh   , R, R(rd) = (int64_t)(((__int128_t)src1 * (__int128_t)src2) >> 64));
    INSTPAT("0000001 ????? ????? 010 ????? 01100 11", mulhsu , R, R(rd) = (int64_t)(((__int128_t)src1 * (__uint128_t)src2) >> 64));
    INSTPAT("0000001 ????? ????? 011 ????? 01100 11", mulhu  , R, R(rd) = (int64_t)(((__uint128_t)src1 * (__uint128_t)src2) >> 64));
    INSTPAT("0000001 ????? ????? 100 ????? 01100 11", div    , R, R(rd) = (int64_t)src1 / (int64_t)src2);
    INSTPAT("0000001 ????? ????? 101 ????? 01100   11", divu   , R, R(rd) = src1 / src2);
    INSTPAT("0000001 ????? ????? 110 ????? 01100 11", rem    , R, R(rd) = (int64_t)src1 % (int64_t)src2);
    INSTPAT("0000001 ????? ????? 111 ????? 01100 11", remu   , R, R(rd) = src1 % src2);

    INSTPAT("0000001 ????? ????? 000 ????? 01110 11", mulw   , R, R(rd) = TRUNC((int32_t)src1 * (int32_t)src2));
    INSTPAT("0000001 ????? ????? 100 ????? 01110 11", divw   , R, R(rd) = TRUNC((int32_t)src1 / (int32_t)src2));
    INSTPAT("0000001 ????? ????? 101 ????? 01110 11", divuw  , R, R(rd) = TRUNC((uint32_t)src1 / (uint32_t)src2));
    INSTPAT("0000001 ????? ????? 110 ????? 01110 11", remw   , R, R(rd) = TRUNC((int64_t)src1 % (int64_t)src2));
    INSTPAT("0000001 ????? ????? 111 ????? 01110 11", remuw  , R, R(rd) = TRUNC((uint32_t)src1 % (uint32_t)src2));

    //Invalid Opcode
    INSTPAT("??????? ????? ????? ??? ????? ????? ??", unk    , N, printf(ANSI_FMT("Unknown Inst!\n", ANSI_FG_RED)), HALT(s->pc, -1));
    INSTPAT_END();

    R(0) = 0;

    return;
}
